    // Checks whether same movie ID exists in the table or not
    public Boolean mID_Check(int id)
    {
        Boolean valid=true;
        int row=MTable.getRowCount();
        for(int i = 0; i < row; i++){
            if(id==Integer.parseInt((String)MTable.getValueAt(i,0))){
                valid=false;
            }
        }
        return valid;
    }
    
    public String[][] get_tableData()
    {
        String[][] movieList = new String[MTable.getRowCount()][8];  
            for (int i =0; i<MTable.getRowCount(); i++){
                for (int j =0; j<8; j++){
                    movieList[i][j]=(String) (MTable.getValueAt(i,j));
                } 
            }
               return movieList;
    }
    
    public String generate_list(ArrayList<String> match, String genre)
    {
        String output = "There are "+ match.size() + " movies in " + genre +" genre : \n" ;
        
        for(int i=0; i<match.size(); i++){
            output +=i +" .  " + match.get(i)+ "\n";
        };
        return output;
    }

