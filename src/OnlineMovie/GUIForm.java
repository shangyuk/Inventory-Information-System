    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        mID=(get_mID());
        mName=get_mName();
        duration=get_duration();
        mGenre=get_mGenre();
        rating=get_rating();
        price=get_price();
        tAudience=get_tAudience();
        discount=get_discount();
        if(mID<0){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please enter a positive whole number for Movie ID!");
        }
        else if(mID_Check(mID)==false){
            JOptionPane.showMessageDialog(jPanel2,"Movie ID already exists! Please enter a different number for Movie ID!");
        }
        else if(mName.isEmpty()){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please enter a Movie Name.");
        }
        else if(duration<0){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please enter duration of the Movie!");
        }
        else if(price<0){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please enter price of the Movie!");
        }
        else if(discount<0 || discount>price){
            if(discount>price){
                JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Discount value exceeds price! Please enter a valid discount amount!");
            }
            else{
                JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please enter a valid discount amount!");
            }
        }
        else if(mGenre.equals(" ")){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please select a genre for the Movie!");
        }
        else if(tAudience.equals("")){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please select a targeted audience!");
        }
        else
        {
            DefaultTableModel model = (DefaultTableModel)MTable.getModel();
            model.addRow(new Object[]
            {
                String.valueOf(mID),mName,mGenre,String.valueOf(duration),tAudience,String.valueOf(price), String.valueOf(discount),String.valueOf(rating)
            }
            );
            JOptionPane.showMessageDialog(jPanel2,"Record Added!");
        }
    }//GEN-LAST:event_AddBtnActionPerformed

