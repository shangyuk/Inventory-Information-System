    private void jMenuImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuImportActionPerformed
        String filePath = "src/OnlineMovie/imp.csv";
        File file = new File(filePath);
        int success = 0;
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
//          
            DefaultTableModel model = (DefaultTableModel)MTable.getModel();
            Object[] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
               String[] row = lines[i].toString().split(",");
               if(mID_Check(Integer.parseInt(row[0]))==true){
                    model.addRow(row);
                    success = 1;
                }
            }
            if (success == 1){
                JOptionPane.showMessageDialog(jPanel2,"Rows have been added to table");
            }else{
                JOptionPane.showMessageDialog(jPanel2,"Values already exist in the table");
            }
                
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(jPanel2,"No file found in the location of drive.");
        }
    }//GEN-LAST:event_jMenuImportActionPerformed

