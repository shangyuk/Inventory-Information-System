    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed

        if(get_searchPrice()<0){
            JOptionPane.showMessageDialog(jPanel2,"Invalid Input! Please enter valid price to search!");
        }
        else{
            String[][] movieList = get_tableData();

            SelectionSort.sort(movieList);
            int ans=BinarySearch.search(movieList, 0, (movieList.length)-1, get_searchPrice());

//          
            if(ans!=-1){
                JOptionPane.showMessageDialog(jPanel2,"Movie ID: " + movieList[ans][0] + "\nMovie Name: " + movieList[ans][1] + "\nMovie Genre: " + movieList[ans][2] + "\nMovie Duration: " + movieList[ans][3] + "\nTarget Audience: " + movieList[ans][4] + "\nRating: " + movieList[ans][7] + "\nDiscount: " + movieList[ans][6]);
            }
            else{
                JOptionPane.showMessageDialog(jPanel2,"There is no Movie available in the given price!");
            }
        }
    }//GEN-LAST:event_SearchBtnActionPerformed

