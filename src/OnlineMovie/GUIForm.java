    private void jButtonDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDisplayActionPerformed
        mGenre1 = get_mGenre1();
        ArrayList<String> match = new ArrayList();
        String[][] movieList = get_tableData();
        if(mGenre1.equals(" ")){
            JOptionPane.showMessageDialog(jPanel2,"Please select a genre to generate lsit of movies");
        }else{
            for (String[] movieItem : movieList) {
                if (movieItem[2].equals(mGenre1)) {
                    match.add(movieItem[1]);
                }    
            }
        }
        if (match.isEmpty()){
            JOptionPane.showMessageDialog(jPanel2,"N0 movies found from "+ mGenre1 +" Genre" );
        }else{
            String output = generate_list(match, mGenre1);
            JOptionPane.showMessageDialog(jPanel2, output);
        }   
    }//GEN-LAST:event_jButtonDisplayActionPerformed

